#include <stdio.h>

int	ft_find_next_prime(int nb)
{
	long	i;

	if (nb <= 1)
		return (2);
	i = 2;
	while (i * i <= nb)
	{
		if (nb % i == 0)
		{
			nb++;
			i = 2;
		}
		else
			i++;
	}
	return (nb);
}
