void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	sta;

	i = 0;
	sta = 0;
	while (i < size / 2)
	{
		sta = tab[i];
		tab[i] = tab[size - i - 1];
		tab[size - i - 1] = sta;
		i++;
	}
}
